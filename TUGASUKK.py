nilai_A = 70
nilai_B = 80
operator = "*"

if(operator == "+"): 
      hasil = nilai_A + nilai_B
      print(hasil)
elif(operator == "-"): 
      hasil = nilai_A - nilai_B
      print(hasil)
elif(operator == "*"): 
      hasil = nilai_A * nilai_B
      print(hasil)
elif(operator == "/"): 
      hasil = nilai_A / nilai_B
      print(hasil)      